def extract_specs(parser: argparse.ArgumentParser):
    """Introspect an argparse parser into picklable widget metadata.

    Returns ``(order, bools, values)`` where ``order`` is the dest list, ``bools``
    maps a boolean dest to its store_true/store_false option strings, and
    ``values`` maps a value dest to its option string, type, choices and default.
    """
    bools: dict[str, dict] = {}
    values: dict[str, dict] = {}
    order: list[str] = []
    for a in parser._actions:
        dest = a.dest
        if dest in SKIP_DESTS:
            continue
        if isinstance(a, (argparse._StoreTrueAction, argparse._StoreFalseAction)):
            e = bools.setdefault(dest, {"true": None, "false": None,
                                        "default": parser.get_default(dest), "help": ""})
            if isinstance(a, argparse._StoreTrueAction):
                e["true"] = a.option_strings[0]
            else:
                e["false"] = a.option_strings[0]
            e["help"] = e["help"] or (a.help or "")
            if dest not in order:
                order.append(dest)
        else:
            tname = "str"
            if a.type is int:
                tname = "int"
            elif a.type is float:
                tname = "float"
            values[dest] = {
                "opt": a.option_strings[0] if a.option_strings else dest,
                "type": tname,
                "choices": list(a.choices) if a.choices else None,
                "default": parser.get_default(dest),
                "help": a.help or "",
            }
            order.append(dest)
    return order, bools, values


