@st.cache_resource(show_spinner=False)
def get_variant_options():
    """(key, label) for every selectable variant, in canonical display order."""
    if str(EXP_DIR) not in sys.path:
        sys.path.insert(0, str(EXP_DIR))
    import Moving_gate_exp as M
    opts = []
    for key, label in M.ALL_VARIANTS.items():
        if key == "contextual_ssm":
            label = "🆕 ContextualDeepSSM"
        opts.append((key, label))
    return opts


def render_widget(dest, bools, values):
    """Render one widget; return the raw widget value."""
    if dest in bools:
        e = bools[dest]
        return st.checkbox(dest, value=bool(e["default"]), help=e["help"], key=f"w_{dest}")
    e = values[dest]
    default, tname, choices, helptext = e["default"], e["type"], e["choices"], e["help"]
    if dest == "device" and not choices:
        choices = DEVICE_CHOICES
    if choices:
        idx = choices.index(default) if default in choices else 0
        return st.selectbox(dest, choices, index=idx, help=helptext, key=f"w_{dest}")
    if tname == "int":
        if default is None:
            return st.text_input(f"{dest} (blank = auto)", value="", help=helptext, key=f"w_{dest}")
        return st.number_input(dest, value=int(default), step=1, help=helptext, key=f"w_{dest}")
    if tname == "float":
        # text input preserves scientific notation / exact small values
        return st.text_input(dest, value=str(default), help=helptext, key=f"w_{dest}")
    return st.text_input(dest, value="" if default is None else str(default),
                         help=helptext, key=f"w_{dest}")


def build_argv(order, bools, values, widget_vals, run_id):
    """Turn widget values into a minimal argv (only non-default flags)."""
    argv: list[str] = ["--no_show_plots", "--run_id", run_id]
    warnings: list[str] = []
    for dest in order:
        if dest == "run_id":
            continue
        v = widget_vals[dest]
        if dest in bools:
            e = bools[dest]
            default = bool(e["default"])
            if bool(v) == default:
                continue
            if v and e["true"]:
                argv.append(e["true"])
            elif (not v) and e["false"]:
                argv.append(e["false"])
            continue
        e = values[dest]
        opt, tname, choices, default = e["opt"], e["type"], e["choices"], e["default"]
        if dest == "device" and not choices:
            choices = DEVICE_CHOICES
        if choices:
            if v != default:
                argv += [opt, str(v)]
        elif tname == "int" and default is None:
            txt = str(v).strip()
            if txt:
                try:
                    int(txt)
                    argv += [opt, txt]
                except ValueError:
                    warnings.append(f"{dest}: '{txt}' is not an int — skipped.")
        elif tname == "int":
            if int(v) != int(default):
                argv += [opt, str(int(v))]
        elif tname == "float":
            txt = str(v).strip()
            if txt == "":
                continue
            try:
                if float(txt) != float(default):
                    argv += [opt, txt]
            except ValueError:
                warnings.append(f"{dest}: '{txt}' is not a float — skipped.")
        else:  # str
            dflt = "" if default is None else str(default)
            if str(v).strip() != dflt:
                argv += [opt, str(v).strip()]
    return argv, warnings


def launch(argv, run_id):
    logfile = LOG_DIR / f"{run_id}.log"
    cmd = [sys.executable, "-u", str(SCRIPT)] + argv
    logf = open(logfile, "w", buffering=1)
    proc = subprocess.Popen(cmd, stdout=logf, stderr=subprocess.STDOUT,
                            cwd=str(ROOT), text=True)
    st.session_state.update(
        proc=proc, proc_logf=logf, proc_log=str(logfile),
        proc_cmd=" ".join(shlex.quote(c) for c in cmd), proc_run_id=run_id,
    )


def tail(path, max_lines=500):
    try:
        lines = Path(path).read_text(errors="replace").splitlines()
    except FileNotFoundError:
        return "(waiting for output…)"
    return "\n".join(lines[-max_lines:]) or "(no output yet)"


def main():
    st.set_page_config(page_title="PB+SSM gate launcher", layout="wide")
    st.title("🧭 Contextual PB + SSM — experiment launcher")
    st.caption(f"`{SCRIPT.relative_to(ROOT)}`  •  python: `{sys.executable}`")

    try:
        order, bools, values = get_specs()
        specs_error = None
    except Exception as exc:  # pragma: no cover
        order, bools, values = [], {}, {}
        specs_error = exc

    tab_launch, tab_browse = st.tabs(["🚀 Launch run", "📊 Browse runs"])

    # ── Launch tab ──────────────────────────────────────────────
    with tab_launch:
        if specs_error is not None:
            st.error(f"Could not import the experiment / build the parser:\n\n{specs_error}")
        else:
            assigned: set[str] = set()
            with st.form("launch_form", border=False):
                st.markdown("#### ① Variants to run")
                st.caption("Tick the controllers to train & compare in this run.")
                vopts = get_variant_options()
                vcols = st.columns(3)
                for i, (vkey, vlabel) in enumerate(vopts):
                    vcols[i % 3].checkbox(
                        vlabel,
                        value=(vkey in VARIANT_DEFAULT_ON),
                        help=VARIANT_HELP.get(vkey),
                        key=f"var_{vkey}",
                    )
                st.divider()
                st.markdown("#### ② Parameters")
                c1, c2 = st.columns(2)
                halves = (GROUPS[: len(GROUPS) // 2 + 1], GROUPS[len(GROUPS) // 2 + 1:])
                for col, half in ((c1, halves[0]), (c2, halves[1])):
                    with col:
                        for title, pred, expanded in half:
                            members = [d for d in order if d not in assigned and pred(d)]
                            if not members:
                                continue
                            assigned.update(members)
                            with st.expander(title, expanded=expanded):
                                for d in members:
                                    render_widget(d, bools, values)
                leftover = [d for d in order if d not in assigned]
                if leftover:
                    with c2:
                        with st.expander("Other", expanded=False):
                            for d in leftover:
                                render_widget(d, bools, values)
                submitted = st.form_submit_button(
                    "🚀 Launch run", type="primary", width="stretch")

            if submitted:
                widget_vals = {d: st.session_state.get(f"w_{d}") for d in order}
                run_id = (widget_vals.get("run_id") or "").strip() or f"ui_{datetime.now():%Y%m%d_%H%M%S}"
                selected = [k for k, _ in get_variant_options() if st.session_state.get(f"var_{k}")]
                if not selected:
                    st.error("Select at least one variant to run under ① above.")
                else:
                    argv, warns = build_argv(order, bools, values, widget_vals, run_id)
                    argv += ["--variants", ",".join(selected)]
                    for w in warns:
                        st.warning(w)
                    launch(argv, run_id)
                    st.success(
                        f"Launched **{run_id}** — {len(selected)} variant(s): "
                        f"{', '.join(selected)}  (PID {st.session_state['proc'].pid}).")

            proc = st.session_state.get("proc")
            if proc is not None:
                ret = proc.poll()
                running = ret is None
                head = st.columns([3, 1, 1])
                head[0].markdown(f"**Run:** `{st.session_state.get('proc_run_id', '?')}`")
                if running:
                    head[1].markdown("🟢 **running**")
                    if head[2].button("⏹ Stop", width="stretch"):
                        proc.terminate()
                        st.toast("Sent terminate signal.")
                else:
                    (head[1].success if ret == 0 else head[1].error)(
                        "✅ finished" if ret == 0 else f"❌ exit {ret}")
                with st.expander("Command", expanded=False):
                    st.code(st.session_state.get("proc_cmd", ""), language="bash")
                auto = st.checkbox("Auto-refresh log (2s)", value=running)
                st.code(tail(st.session_state.get("proc_log", "")), language="text")
                if auto and running:
                    time.sleep(2)
                    _rerun()

    # ── Browse tab ──────────────────────────────────────────────
    with tab_browse:
        runs = sorted([p for p in RUNS_DIR.iterdir() if p.is_dir()],
                      key=lambda p: p.stat().st_mtime, reverse=True)
        if not runs:
            st.info(f"No runs yet under `{RUNS_DIR.relative_to(ROOT)}`. Launch one from the first tab.")
        else:
            names = [p.name for p in runs]
            sel = st.selectbox("Run", names, index=0)
            run_dir = RUNS_DIR / sel
            top = st.columns([1, 1, 2])
            if top[0].button("🔁 Re-plot (plot_only)"):
                launch(["--no_show_plots", "--plot_only", sel], f"replot_{sel}")
                st.success(f"Re-plotting `{sel}` — see the Launch tab for progress.")
            top[1].markdown(f"📁 `{run_dir.relative_to(ROOT)}`")

            metrics_path = run_dir / "metrics.json"
            if metrics_path.exists():
                metrics = json.loads(metrics_path.read_text())
                cols = ["success_rate", "wall_success_rate", "goal_success_rate",
                        "avg_terminal_dist", "avg_abs_cross_error", "avg_cost"]
                table = []
                for variant, m in metrics.items():
                    row = {"variant": variant}
                    row.update({c: round(float(m[c]), 4) for c in cols if c in m})
                    table.append(row)
                st.subheader("Test metrics")
                st.dataframe(table, width="stretch", hide_index=True)

            interp = run_dir / "interpretation.txt"
            if interp.exists():
                with st.expander("interpretation.txt", expanded=True):
                    st.text(interp.read_text())
            cfg = run_dir / "config.json"
            if cfg.exists():
                with st.expander("config.json", expanded=False):
                    st.json(json.loads(cfg.read_text()))

            gifs = sorted(run_dir.glob("*.gif"))
            pngs = sorted(run_dir.glob("*.png"))
            pdfs = sorted(run_dir.glob("*.pdf"))
            if gifs:
                st.subheader("Animations")
                for g in gifs:
                    st.image(str(g), caption=g.name)
            if pngs:
                st.subheader("Figures")
                img_cols = st.columns(2)
                for i, p in enumerate(pngs):
                    img_cols[i % 2].image(str(p), caption=p.name, width="stretch")
            if pdfs:
                st.subheader("PDFs")
                for p in pdfs:
                    st.download_button(f"⬇ {p.name}", p.read_bytes(),
                                       file_name=p.name, key=f"dl_{p.name}")


if __name__ == "__main__":
    if _under_streamlit_run():
        main()
    else:
        # Launched as a plain script (e.g. `python launcher_app.py` or an IDE
        # Run button). Streamlit needs its own runtime, so re-exec through the
        # streamlit CLI instead of emitting "missing ScriptRunContext" warnings.
        import subprocess

        print("This is a Streamlit app — relaunching via `streamlit run`…\n")
        raise SystemExit(
            subprocess.call(
                [sys.executable, "-m", "streamlit", "run", __file__, *sys.argv[1:]]
            )
        )
