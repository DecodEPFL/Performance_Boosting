def render_widget(dest, bools, values, override=_NO_OVERRIDE):
    """Render one widget; return the raw widget value.

    ``override`` (when not ``_NO_OVERRIDE``) replaces the parser default as the
    widget's initial value — used to apply user-saved launcher defaults.
    """
    has_ovr = override is not _NO_OVERRIDE
    if dest in bools:
        e = bools[dest]
        val = bool(override) if has_ovr else bool(e["default"])
        return st.checkbox(dest, value=val, help=e["help"], key=f"w_{dest}")
    e = values[dest]
    default, tname, choices, helptext = e["default"], e["type"], e["choices"], e["help"]
    if dest == "device" and not choices:
        choices = DEVICE_CHOICES
    if choices:
        cur = override if has_ovr else default
        idx = choices.index(cur) if cur in choices else 0
        return st.selectbox(dest, choices, index=idx, help=helptext, key=f"w_{dest}")
    if tname == "int":
        if default is None:
            val = str(override) if (has_ovr and override not in (None, "")) else ""
            return st.text_input(f"{dest} (blank = auto)", value=val, help=helptext, key=f"w_{dest}")
        val = int(override) if has_ovr else int(default)
        return st.number_input(dest, value=val, step=1, help=helptext, key=f"w_{dest}")
    if tname == "float":
        if default is None:
            val = str(override) if (has_ovr and override not in (None, "", "None")) else ""
            return st.text_input(f"{dest} (blank = auto)", value=val, help=helptext, key=f"w_{dest}")
        # text input preserves scientific notation / exact small values
        val = str(override) if has_ovr else str(default)
        return st.text_input(dest, value=val, help=helptext, key=f"w_{dest}")
    val = str(override) if has_ovr else ("" if default is None else str(default))
    return st.text_input(dest, value=val, help=helptext, key=f"w_{dest}")


def build_argv(order, bools, values, widget_vals, run_id):
    """Turn widget values into a minimal argv (only non-default flags)."""
    argv: list[str] = ["--no_show_plots", "--run_id", run_id]
    warnings: list[str] = []
    for dest in order:
        if dest == "run_id":
            continue
        v = widget_vals[dest]
        if dest in bools:
            e = bools[dest]
            default = bool(e["default"])
            if bool(v) == default:
                continue
            if v and e["true"]:
                argv.append(e["true"])
            elif (not v) and e["false"]:
                argv.append(e["false"])
            continue
        e = values[dest]
        opt, tname, choices, default = e["opt"], e["type"], e["choices"], e["default"]
        if dest == "device" and not choices:
            choices = DEVICE_CHOICES
        if choices:
            if v != default:
                argv += [opt, str(v)]
        elif tname == "int" and default is None:
            txt = str(v).strip()
            if txt:
                try:
                    int(txt)
                    argv += [opt, txt]
                except ValueError:
                    warnings.append(f"{dest}: '{txt}' is not an int — skipped.")
        elif tname == "int":
            if int(v) != int(default):
                argv += [opt, str(int(v))]
        elif tname == "float":
            txt = str(v).strip()
            if txt == "" or txt.lower() == "none":
                continue
            try:
                parsed = float(txt)
            except ValueError:
                warnings.append(f"{dest}: '{txt}' is not a float — skipped.")
                continue
            if default is None or parsed != float(default):
                argv += [opt, txt]
        else:  # str
            dflt = "" if default is None else str(default)
            if str(v).strip() != dflt:
                argv += [opt, str(v).strip()]
    return argv, warnings


def launch(argv, run_id):
    logfile = LOG_DIR / f"{run_id}.log"
    cmd = [sys.executable, "-u", str(SCRIPT)] + argv
    logf = open(logfile, "w", buffering=1)
    proc = subprocess.Popen(cmd, stdout=logf, stderr=subprocess.STDOUT,
                            cwd=str(ROOT), text=True)
    st.session_state.update(
        proc=proc, proc_logf=logf, proc_log=str(logfile),
        proc_cmd=" ".join(shlex.quote(c) for c in cmd), proc_run_id=run_id,
    )


